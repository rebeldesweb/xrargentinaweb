 		<p>Entendemos que debemos auto-organizarnos para lograr el cambio. ¡Cualquier persona o grupo que respete los 10 principios y valor y promueva los 3 objetivos puede organizarse de manera autónoma y actuar en nombre y espíritu de Extinction Rebellion! De esta manera, el poder se descentraliza, lo que significa que no es necesario pedir permiso a un grupo o autoridad central.<br><br>
		Nos organizamos en equipos locales y en grupos de afinidad. Los grupos locales tienen como objetivo crear la comunidad de Extinction Rebellion en sus lugares. XR Argentina tiene como objetivo crear la comunidad en Argentina.<br><br>
		Los grupos de afinidad son grupos para activar la desobediencia civil, formados por personas que se unen para preparar y realizar acciones directas.<br><br>
		XR Argentina se organiza basado en el concepto de holocracia, en donde la autoridad y la toma de decisiones está descentralizada. Aunque creemos que algún tipo de jerarquía es necesaria y no somos una holocracia total, seguimos sus principios al estar convencidos que esta forma de organización es la necesaria para actuar con confianza y celeridad y que la información fluya a través de la organización.<br><br>
		Tenemos una <a href="https://base.xrargentina.org/t/constitucion-xr-argentina/33"> constitución </a> con reglas para auto-organizarnos. La constitución presenta las reglas centrales, el formato de estructura y los procesos esenciales para la autoorganización. Es el documento al que debemos volver cada vez que surja algún a duda o conflicto en cómo nos estructuramos y cómo tomamos decisiones. Se basa en el concepto de autoridad distribuida. La autoridad no está concentrada en personas o equipos, sino que está distribuida en roles con objetivos y responsabilidad claras y establecidas, otorgándole a quién ocupa el rol en determinado momento, la autoridad necesaria para tomar las decisiones y actuar, y sienta las bases de las expectativas de lo que el resto del equipo espera del trabajo. Las decisiones son tomadas por quien tiene un rol de trabajo. Los roles se agrupan dentro de equipos y puede haber equipos dentro de equipos tanto como sea necesario. En XR, la autoridad se gana. Las personas pueden participar de los grupos únicamente si cumplen de manera efectiva un rol dentro del equipo.<br><br>
		XR se descentraliza en círculos (equipos de trabajo). Cada uno de los cuales está formado por sub-equipos, que también se dividen en sub-círculos y así sucesivamente. Cada equipo de trabajo tiene roles con mandatos definidos. Cada equipo tiene un coordinador que ayuda a que el flujo de información pase a través de los círculos asistiendo a reuniones de círculos más amplios. El Equipo Central está formado por coordinadores de cada uno de estos círculos más amplios y algunos otros. Se reúnen semanalmente para discutir los problemas más amplios del sistema y para promover el intercambio de información dentro de la organización.<br><br>
